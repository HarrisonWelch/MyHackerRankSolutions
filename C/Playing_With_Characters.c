#include <stdio.h>
#include <string.h>
#include <math.h>
#include <stdlib.h>

int main() 
{

    /* Enter your code here. Read input from STDIN. Print output to STDOUT */ 
    char s1[100];
    char s2[100];
    char s3[100];

    scanf("%[^\n]%*c", s1);
    scanf("%[^\n]%*c", s2);
    scanf("%[^\n]%*c", s3);

    printf("%s\n",s1);
    printf("%s\n",s2);
    printf("%s\n",s3);

    return 0;
}
